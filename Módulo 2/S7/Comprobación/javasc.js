// solicitar el ingreso de dos números
var numUno = parseInt(prompt("Ingrese primer número"));
var numDos = parseInt(prompt("Ingrese segundo número"));
// calcular cual es mayor o si tienen el mismo valor
if (numUno > numDos) {
    alert("El primer número es mayor que el segundo")
} else if (numUno < numDos) {
    alert ("El segundo número es mayor que el primero")
} else {
    alert ("Ambos números son iguales")
}    
